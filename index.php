		<p>Is that you, professor? We gasped for breath. Stupefaction more than fear made us dumb and motionless. The animal gained on us, sporting with the waves. It made the round of the frigate, which was then making fourteen knots, and enveloped it with its electric rings like luminous dust. Then it moved away two or three miles, leaving a phosphorescent track, like those volumes of steam that the express trains leave behind. All at once from the dark line of the horizon whither it retired to gain its momentum, the monster rushed suddenly toward the Abraham Lincoln with alarming rapidity, stopped suddenly about twenty feet from the hull, and died out-not diving under the water, for its brilliancy did not abate-but suddenly, and as if the source of this brilliant emanation was exhausted. Then it reappeared on the other side of the vessel, as if it had turned and slid under the hull. Any moment a collision might have occurred which would have been fatal to us. However, I was astonished at the maneuvers of the frigate. She fled and did not attack. Never mind, said Ned Land urgently. On the 28th of February, when at noon the Nautilus came to the surface of the sea, in 9 4' north latitude, there was land in sight about eight miles to westward. The first thing I noticed was a range of mountains about two thousand feet high, the shapes of which were most capricious. On taking the bearings, I knew that we were nearing the island of Ceylon, the pearl which hangs from the lobe of the Indian Peninsula.</p>
